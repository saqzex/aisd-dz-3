# по массе по возрастанию;
# если масса одинаковая, то по кличке в алфавитном порядке.
def comes_before(a, b):
    if a[2] != b[2]:
        return a[2] < b[2]
    return a[0] < b[0]

def insertion_sort(arr):
    result = arr[:]  # копия списка
    for i in range(1, len(result)):
        current = result[i]
        j = i - 1

        while j >= 0 and comes_before(current, result[j]):
            result[j + 1] = result[j]
            j -= 1

        result[j + 1] = current
    return result

def print_animals(title, arr):
    print(title)
    for animal in arr:
        print(f"{animal[0]} - {animal[1]} - {animal[2]} кг")
    print()

def find_by_weight(arr, weight):
    found = []
    for animal in arr:
        if animal[2] == weight:
            found.append(animal)
    return found

def group_by_same_weight(arr):
    groups = {}
    for animal in arr:
        weight = animal[2]
        if weight not in groups:
            groups[weight] = []
        groups[weight].append(animal)
    return groups

